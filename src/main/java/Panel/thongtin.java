    public void AddCMT()
    {
        if(home.getAccount().getID() <=0) home.callLogin();
        else
        {
            Cmt cmt = new Cmt();
            LocalDateTime currentDateTime = LocalDateTime.now();
            
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm dd/MM/yyyy");
            String formattedDate = currentDateTime.format(formatter);
            
            cmt.setTextAll(home.getAccount().getName(), jTextField1.getText(), formattedDate, home.getAccount().getURLAva());
            Comment c = new Comment();
            c.addComment(home.getAccount().getID(), m.getID(), jTextField1.getText(), formattedDate);
            jPanelCMT.add(cmt);
            jPanelCMT.revalidate();
            jPanelCMT.repaint();
        }    
    }
    
    public void callCmt()
    {
        jPanelCMT.removeAll();
        List<Comment> lstc = cmt.getMovieComments(m.getID());
        for(int i = 0;i<lstc.size();i++)
        {
            Cmt cmt = new Cmt();
            AccountData acc = new AccountData();
            cmt.setTextAll(acc.setAccount(lstc.get(i).getUserID()).getName(), lstc.get(i).getContent(), lstc.get(i).getTimeCmt() , acc.setAccount(lstc.get(i).getUserID()).getURLAva());
            jPanelCMT.add(cmt);
            jPanelCMT.revalidate();
            jPanelCMT.repaint();
        }
        
    }
    
